#!/usr/bin/env python3

"""
a function that concat 2 string
"""


def concat(str1: str, str2: str) -> str:
    """
    Concatenate 2 strings

    Args:
        str1: (str) - string 1
        str2: (str) - string 2

    Returns:
        returns the concatenated strings
    """
    return (str1 + str2)
