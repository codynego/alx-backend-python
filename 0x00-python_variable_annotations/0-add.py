#!/usr/bin/env python3

"""
A add function
"""


def add(a: float, b: float) -> float:
    """
    A function that returns the sum of 2 arguments

    Args:
        a: (float) - the first argument
        b: (float) - the second argument

    Return:
        returns the sum of 2 floats (a and b)
    """
    return (a + b)
